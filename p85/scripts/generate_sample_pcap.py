#!/usr/bin/env python3
import struct
import os


def build_modbus_packet(tid, pid, uid, fc, payload):
    length = 2 + len(payload)
    header = struct.pack('>HHHB', tid, pid, length, uid)
    return header + bytes([fc]) + payload


def generate_sample_pcap():
    packets = []

    samples = [
        (0x0001, 0x0000, 1, 0x03, bytes([0x00, 0x00, 0x00, 0x0A])),
        (0x0002, 0x0000, 1, 0x03, bytes([0x14, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0A, 0x0B, 0x0C, 0x0D, 0x0E, 0x0F, 0x10, 0x11, 0x12, 0x13, 0x14])),
        (0x0003, 0x0000, 1, 0x41, bytes([0x00, 0x01, 0x01, 0x66, 0x45, 0x32, 0x10, 0x41, 0x41, 0xF5, 0xC3])),
        (0x0004, 0x0000, 1, 0x42, bytes([0x00, 0x10, 0x00, 0x00, 0x00, 0x64])),
        (0x0005, 0x0000, 1, 0x44, bytes([0x01])),
        (0x0006, 0x0000, 1, 0x45, bytes([0x00, 0xFF])),
        (0x0007, 0x0000, 2, 0x06, bytes([0x00, 0x10, 0x01, 0x00])),
        (0x0008, 0x0000, 2, 0x01, bytes([0x00, 0x00, 0x00, 0x08])),
    ]

    for tid, pid, uid, fc, payload in samples:
        pkt = build_modbus_packet(tid, pid, uid, fc, payload)
        packets.append(pkt)

    return packets


if __name__ == '__main__':
    print("示例 Modbus 数据包:")
    packets = generate_sample_pcap()
    for i, pkt in enumerate(packets):
        print(f"数据包 {i+1}: {pkt.hex()}")

    os.makedirs('uploads', exist_ok=True)
    print("\n注意: 此脚本仅生成示例数据包内容")
    print("如需生成真实的 PCAP 文件，请使用 Wireshark 或 tcpdump 捕获网络流量")
    print("或使用 scapy 库构造完整的 PCAP 文件")
