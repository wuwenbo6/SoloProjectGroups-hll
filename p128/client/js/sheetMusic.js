    getAllSheets() {
        return this.sheets.map(sheet => ({
            id: sheet.id,
            title: sheet.title,
            difficulty: sheet.difficulty,
            key: sheet.key,
            description: sheet.description
        }));
    },

    getSheetById(id) {
        return this.sheets.find(sheet => sheet.id === id) || null;
    },

    getNotesFlat(sheetId) {
        const sheet = this.getSheetById(sheetId);
        if (!sheet) return [];
        
        const notes = [];
        sheet.measures.forEach(measure => {
            measure.notes.forEach(note => {
                notes.push({
                    ...note,
                    measureId: measure.id,
                    noteNumber: MidiManager.nameToNote(note.note)
                });
            });
        });
        return notes;
    },

    getExpectedNote(sheetId, noteIndex) {
        const notes = this.getNotesFlat(sheetId);
        return notes[noteIndex] || null;
    },

    getNextExpectedNotes(sheetId, currentIndex, count = 4) {
        const notes = this.getNotesFlat(sheetId);
        return notes.slice(currentIndex, currentIndex + count);
    },

    getTotalNotes(sheetId) {
        return this.getNotesFlat(sheetId).length;
    }
